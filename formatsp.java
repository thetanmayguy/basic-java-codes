class formatsp
{
    public static void main(String args[])
    {
        System.out.printf("int = %d\n",5);
        System.out.printf("float = %.2f\n",5.5);
        System.out.printf("char = %c\n",'a');
        System.out.printf("int hex = %x\n",11);
        System.out.printf("int oct = %o\n",11);
    }
}