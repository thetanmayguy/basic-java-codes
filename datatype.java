class datatype
{
    public static void main(String args[])
    {
        int n=10;
        System.out.println("int="+n);
        char c='a';
        System.out.println("char="+c);
        float f=3.4f;
        System.out.println("float="+f);
        boolean b=true,d=false;
        System.out.println("boolean="+b+" "+d);
    }
}